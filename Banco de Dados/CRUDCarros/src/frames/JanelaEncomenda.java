    public void efetuarEncomenda(){
        String cpf = campoCPF.getText();
        cpf = cpf.replace(".", "");
        cpf = cpf.replace("-", "");
        if(filtro.buscarIdCLiente(cpf) == 0){
            JOptionPane.showMessageDialog(this, "CLIENTE AINDA NÃO CADASTRADO", "ERRO!", JOptionPane.ERROR_MESSAGE);
        }else{
            Encomenda encomenda = new Encomenda();
            encomenda.setData_Entrega(Date.valueOf(jFormattedTextField1.getText()));
            encomenda.setIdCarro(idCarro);
            encomenda.setIdCliente(filtro.buscarIdCLiente(cpf));
            encomenda.setIdConcessionaria(idConcessionaria);
            enDAO.inserir(encomenda);
            JOptionPane.showMessageDialog(this, "ENCOMENDA REGISTRADA!");
        }        
    }
    
