    public void inserirEncomenda() {
        int i = Integer.parseInt(campoQntd.getText());

        CarrosDisponiveis car = new CarrosDisponiveis();
        car.setIdCarro(idCarro);
        car.setIdConcessionaria(idConcessionaria);
        while (i > 0) {
            carAO.inserir(car);
            i--;
        }
    }

